export function getTransliteration(zikr: Zikr, lang: string): string {
  if (lang === "de") {
    if (zikr.transliteration_de) return zikr.transliteration_de;
    // Auto-convert English phonetics to German phonetics
    return zikr.transliteration
      .replace(/sh/g, "sch")
      .replace(/Sh/g, "Sch")
      .replace(/j/g, "dsch")
      .replace(/J/g, "Dsch")
      .replace(/y/g, "j")
      .replace(/Y/g, "J");
  }
  if (lang === "es") {
    if (zikr.transliteration_es) return zikr.transliteration_es;
    // Auto-convert English phonetics to Spanish phonetics
    return zikr.transliteration
      .replace(/kh/g, "j")
      .replace(/Kh/g, "J")
      .replace(/j/g, "y")
      .replace(/J/g, "Y");
  }
  if (lang === "pt") {
    if (zikr.transliteration_pt) return zikr.transliteration_pt;
    // Auto-convert English phonetics to Portuguese phonetics
    return zikr.transliteration
      .replace(/sh/g, "ch")
      .replace(/Sh/g, "Ch")
      .replace(/j/g, "dj")
      .replace(/J/g, "Dj");
  }
  if (lang === "hi") {
    if (zikr.transliteration_hi) return zikr.transliteration_hi;
  }
  return zikr.transliteration;
}

